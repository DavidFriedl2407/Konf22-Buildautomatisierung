package org.campus02.calculator;

public class Calculator {


    public static int add(int a, int b) {
        int result=a +b;
        return result;
    }
    public static int subract(int a, int b){
        int result=a -b;
        return result;
    }
    public static int multiply(int a, int b){
        int result=a *b;
        return result;
    }


}
